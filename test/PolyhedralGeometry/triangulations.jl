@testset "Triangulations{$T}" for T in [QQFieldElem, nf_elem]

    pts0 = [1 0; 0 0; 0 1]
    Q0   = convex_hull(T, pts0)
    pts1 = [2 0; 1 0; 0 0; 0 1; 0 2; 1 1]
    Q1   = convex_hull(T, pts1)

    @test all_triangulations(pts0)==[[[1, 2, 3]]]
    @test all_triangulations(Q0)==[[[1, 2, 3]]]

    @test all_triangulations(pts1)==[[[1, 2, 4], [1, 4, 5], [2, 3, 4]],
                        [[1, 2, 4], [1, 4, 6], [2, 3, 4], [4, 5, 6]],
                        [[1, 2, 5], [2, 3, 4], [2, 4, 5]],
                        [[1, 2, 5], [2, 3, 5]],
                        [[1, 2, 6], [2, 3, 4], [2, 4, 5], [2, 5, 6]],
                        [[1, 2, 6], [2, 3, 4], [2, 4, 6], [4, 5, 6]],
                        [[1, 2, 6], [2, 3, 5], [2, 5, 6]],
                        [[1, 2, 6], [2, 3, 6], [3, 4, 6], [4, 5, 6]],
                        [[1, 2, 6], [2, 3, 6], [3, 5, 6]],
                        [[1, 3, 4], [1, 4, 5]],
                        [[1, 3, 4], [1, 4, 6], [4, 5, 6]],
                        [[1, 3, 5]],
                        [[1, 3, 6], [3, 4, 6], [4, 5, 6]],
                        [[1, 3, 6], [3, 5, 6]]]
    @test all_triangulations(Q1)==[[[1, 2, 3]]]




end